# Открываем текстовый файл для чтения с указанной кодировкой
with open('input.txt', 'r', encoding='utf-8') as f:
    # Читаем все слова из файла
    words = f.read().split()

# Открываем текстовый файл для записи с указанной кодировкой
with open('output.txt', 'w', encoding='utf-8') as f:
    for i in range(0, len(words), 10):
        # Получаем текущую группу слов (10 слов или меньше)
        group = words[i:i + 10]

        # Записываем считанные слова в файл, разделяя их пробелами
        f.write(' '.join(group))

        # Проверяем, что это не последняя группа слов
        if i + 10 < len(words):
            # Добавляем абзац
            f.write('\n\n')

